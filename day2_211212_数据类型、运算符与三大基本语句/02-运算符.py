# 算数运算符  四则运算
#  + 求和
print(1 + 2)  # 3

# - 求差
print(2 - 1)  # 1

# * 求积
print(2 * 3)  # 6

# / 求商  除法运算的结果都是浮点型
print(4 / 2)  # 2.0

# ** 幂次运算
print(2 ** 3)  # 8

# // 整除运算,求商  11 除以 3 商 3  余 2
print(11 // 3)  # 3

# % 取余(取模)运算,求余  11 除以 3 商 3  余 2
print(11 % 3)  # 2

# 赋值运算符  =
# 赋值:就是将等号右侧的值传递给等号左侧的变量

# a = 1
# NameError: name 'a' is not defined
# 所有的变量要先赋值,后参与代码执行
# print(a)

# 赋值运算,=左侧一定是变量,等号右侧一定是值或者已经定义的变量
