# 注意: 前边可以有多个变量连等,但是最终只能有一个值
# a = b = c = 1
# print(a, b, c)


# 复合赋值运算符
# 就是将算数运算符,和赋值运算符进行了结合
# +=
a = 1
# NameError: name 'a' is not defined
# 复合赋值运算符左侧一定是已经定义的变量,右侧可以是已经定义的变量,也可以是值.
a += 1  # 等价于 a = a + 1 在此处先计算a + 1 的结果,再将结果赋值给a
print(a)
